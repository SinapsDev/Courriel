import type { GetServerSideProps } from "next";
import styles from "./index.module.css";
import { getProviders, signIn, useSession } from "next-auth/react";
import Head from "next/head";
import type { AppProviders } from "next-auth/providers";
import { Spinner } from "~/components/Spinner";

export default function Home({ providers }: { providers: AppProviders }) {
  const { data: sessionData } = useSession();

  {
    !sessionData && <Spinner />;
  }

  return (
    <>
      <Head>
        <title>Courriel</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/logo-ministere-sante.ico" />
      </Head>
      <div className={styles.headContainer}>
        <LoginContainer {...providers} />
      </div>
    </>
  );
}

export const getServerSideProps: GetServerSideProps = async () => {
  const providers = await getProviders();
  return {
    props: { providers },
  };
};

function LoginContainer(providers: AppProviders) {
  return (
    <div className={styles.loginContainer}>
      <h1 className={styles.mainTitle}>GESTION DES COURRIELS</h1>
      {Object.values(providers).map((provider) => (
        <div
          className={styles.loginButton}
          key={provider.id}
          onClick={() =>
            signIn(provider.id, {
              callbackUrl: `/home`,
            })
          }
        >
          Connectez vous avec google
        </div>
      ))}
    </div>
  );
}
